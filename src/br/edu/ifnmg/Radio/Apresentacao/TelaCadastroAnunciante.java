    public void AnuncSelecionado(Anunciante anunciante) {

        try {

            lerDadosTela();
            AnuncianteBO anuncianteBO = new AnuncianteBO();

            anuncianteBO.alterar(anunciante);

            this.exibirMensagemSucesso("Anunciante Alterado");
            this.limparCamposTela();

        } catch (ParseException ex) {
            Logger.getLogger(TelaLocutor.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(TelaLocutor.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            //this.dispose();
        }

    }
    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed

        // TODO add your handling code here:
        try {

            AnuncianteBO anuncianteBO = new AnuncianteBO();

            this.anunciante.setNome(txNome.getText());

            this.anunciante.setCPF(txtCPF.getText());
            this.anunciante.setTelefone(txtTel.getText());
            this.anunciante.setCidade(txtCidade.getText());
            this.anunciante.setBairro(txtBairro.getText());
            this.anunciante.setRua(txtRua.getText());
            String Numero = txtNum.getText().trim();
            anunciante.setNumResidencia(Integer.parseInt(Numero));

            anuncianteBO.alterar(this.anunciante);

            this.exibirMensagemSucesso("Anunciante Alterado");
            this.limparCamposTela();

        } catch (SQLException ex) {
            Logger.getLogger(TelaLocutor.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            this.dispose();
            ListarAnunciante c = new ListarAnunciante();
            c.setVisible(true);
        }

    }//GEN-LAST:event_btEditarActionPerformed

