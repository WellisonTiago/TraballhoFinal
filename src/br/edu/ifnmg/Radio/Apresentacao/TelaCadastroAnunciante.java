    private void exibirMensagemSucesso(String mensagem) {
        JOptionPane.showMessageDialog(this, mensagem, "Cadastro de Anuncinate", JOptionPane.INFORMATION_MESSAGE);

    }

    private void exibirMensagemErro(String mensagem) {
        JOptionPane.showMessageDialog(this, mensagem, "Erro ao cadastrar Anunciante", JOptionPane.ERROR_MESSAGE);
    }

    private void lerDadosTela() throws ParseException {
        anunciante = new Anunciante();

        anunciante.setNome(txNome.getText().trim());
        anunciante.setCPF(txtCPF.getText().trim());
        anunciante.setTelefone(txtTel.getText().trim());
        anunciante.setCidade(txtCidade.getText().trim());
        anunciante.setBairro(txtBairro.getText().trim());
        anunciante.setRua(txtRua.getText().trim());
        String Numero = txtNum.getText().trim();
        anunciante.setNumResidencia(Integer.parseInt(Numero));
    }

    private void limparCamposTela() {

        this.txNome.setText("");
        this.txtCPF.setText("");
        this.txtTel.setText("");
        this.txtCidade.setText("");
        this.txtBairro.setText("");
        this.txtRua.setText("");
        this.txtNum.setText("");

    }

