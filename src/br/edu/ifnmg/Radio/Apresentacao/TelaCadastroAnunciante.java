    public TelaCadastroAnunciante(Anunciante anunciantes, String tipoTela) {
        initComponents();
        this.anunciante = anunciantes;
        if (tipoTela.equals("cadastro")) {
            this.btEditar.setVisible(false);
            this.lbEditar.setVisible(false);
            this.tbAdiconar.setVisible(true);
            this.lbAnunciantes.setVisible(true);
        } else {
            this.txNome.setText(anunciante.getNome());
            this.txtCPF.setText(anunciante.getCpf());
            this.txtTel.setText(anunciante.getTelefone());
            this.txtCidade.setText(anunciante.getCidade());
            this.txtBairro.setText(anunciante.getBairro());
            this.txtRua.setText(anunciante.getRua());
            txtNum.setText(String.valueOf(anunciante.getNumResidencia()));
            this.btEditar.setVisible(true);
            this.lbEditar.setVisible(true);
            this.tbAdiconar.setVisible(false);
            this.lbAnunciantes.setVisible(false);

        }
    }

