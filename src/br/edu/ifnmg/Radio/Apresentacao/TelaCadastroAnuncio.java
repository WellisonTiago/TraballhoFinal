    public TelaCadastroAnuncio(Anuncio anuncio, String tipoTela) {
        initComponents();
        this.anuncio = anuncio;
        this.carregarComoBox();
        if (tipoTela.equals("cadastro")) {
            this.btAlterar.setVisible(false);
            this.lbAlterarAnuncio.setVisible(false);
            this.btAdicionar.setVisible(true);
            this.lbAnuncio.setVisible(true);
        } else {
            this.txtDescricao.setText(anuncio.getDescricao());
            this.txtQaunPorDia.setText(Integer.toString(anuncio.getQuanPorDia()));
            this.txtDataTerminio.setText(anuncio.getDataFormatadaEncerramento());
            this.btAlterar.setVisible(true);
            this.lbAlterarAnuncio.setVisible(true);
            this.btAdicionar.setVisible(false);
            this.lbAnuncio.setVisible(false);

        }
    }

