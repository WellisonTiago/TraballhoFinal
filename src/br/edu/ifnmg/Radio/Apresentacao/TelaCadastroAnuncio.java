    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        try {

            AnuncioBO anuncioBO = new AnuncioBO();

            String form = "dd/MM/yyyy";
            Date date = new SimpleDateFormat(form).parse(txtDataTerminio.getText().trim());
            this.anuncio.setEncerramentoAnuncio(date);
            this.anuncio.setDescricao(txtDescricao.getText().trim());
            String quantidade = txtQaunPorDia.getText().trim();
            this.anuncio.setQuanPorDia(Integer.parseInt(quantidade));
            posicaoAnuncianteSelecionado = cmbAnunciante.getSelectedIndex();
            anunciante = listaAnunciante.get(posicaoAnuncianteSelecionado);
            anuncio.setIdAnunciante(anunciante.getIdAnunciante());

            anuncioBO.alterar(this.anuncio);

            this.exibirMensagemSucesso("Anuncio Alterado");
            this.limparCamposTela();

        } catch (SQLException ex) {
            Logger.getLogger(TelaCadastroAnuncio.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(TelaCadastroAnuncio.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            this.dispose();
            // TODO add your handling code here
            ListarAnuncios c = new ListarAnuncios();
            c.setVisible(true);
        }
    }//GEN-LAST:event_btAlterarActionPerformed

