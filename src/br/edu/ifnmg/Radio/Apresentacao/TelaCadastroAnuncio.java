    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed
        // TODO add your handling code here:

        try {
            // TODO add your handling code here:
            lerDadosTela();
        } catch (ParseException ex) {
            Logger.getLogger(TelaCadastroAnunciante.class.getName()).log(Level.SEVERE, null, ex);
        }

        AnuncioBO anuncioBO = new AnuncioBO();

        try {
            anuncioBO.criar(anuncio);
            String mensagem = "Anuncio Cadastrado com Sucesso.";
            exibirMensagemSucesso(mensagem);
            this.limparCamposTela();
        } catch (SQLException ex) {
            Logger.getLogger(TelaCadastroAnuncio.class.getName()).log(Level.SEVERE, null, ex);

        } catch (NenhumAnuncianteCadastradoException e) {
            String mensagem = "Erro ao cadastrar Anuncio ! Nenhum Anunciante Cadastardo.";
            this.exibirMensagemErro(mensagem);
        } catch (Exception e) {
            String mensagem = "Erro desconhecido. Entre em contato com o administrador do sistema.";
            this.exibirMensagemErro(mensagem);
            this.limparCamposTela();
        }
    }//GEN-LAST:event_btAdicionarActionPerformed

    public void carregarComoBox() {

        AnuncianteBO anuncianteBO = new AnuncianteBO();
        try {
            listaAnunciante = anuncianteBO.buscarTodos();

        } catch (SQLException ex) {
            Logger.getLogger(TelaCadastroAnuncio.class.getName()).log(Level.SEVERE, null, ex);
        }
        cmbAnunciante.removeAllItems();
        int linhaselecionada = this.cmbAnunciante.getSelectedIndex();
        for (int registro = 0; registro < listaAnunciante.size(); registro++) {
            //  System.out.println(listaAnunciante);
            anunciante = listaAnunciante.get(registro);
            cmbAnunciante.addItem(anunciante.getNome());
            // posicaoAnuncianteSelecionado = cmbAnunciante.getSelectedIndex();
        }
    }
