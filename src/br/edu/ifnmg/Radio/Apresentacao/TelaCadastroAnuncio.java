    private void lerDadosTela() throws ParseException {
        anuncio = new Anuncio();

        anuncio.setDescricao(txtDescricao.getText().trim());
        String quantidade = txtQaunPorDia.getText().trim();
        anuncio.setQuanPorDia(Integer.parseInt(quantidade));
        Date data = new Date(System.currentTimeMillis());
        this.anuncio.setInicoAnuncio(data);

        SimpleDateFormat formata = new SimpleDateFormat("dd/MM/yyyy");

        String form = "dd/MM/yyyy";
        Date date = new SimpleDateFormat(form).parse(txtDataTerminio.getText().trim());

        anuncio.setEncerramentoAnuncio(date);

        posicaoAnuncianteSelecionado = cmbAnunciante.getSelectedIndex();
        anunciante = listaAnunciante.get(posicaoAnuncianteSelecionado);
        anuncio.setIdAnunciante(anunciante.getIdAnunciante());

    }

    private void limparCamposTela() {

        this.txtDescricao.setText("");
        this.txtQaunPorDia.setText("");
        this.txtDataTerminio.setText("");

    }

